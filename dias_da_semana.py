dia = int(input("Digite um número entre 0 e 6:"))

if dia == 0:
    print("Hoje é Domingo!")
elif dia == 1:
    print("Hoje é Segunda-feira!")
elif dia == 2:
    print("Hoje é Terça-feira!")
elif dia == 3:
    print("Hoje é Quarta-feira!")
elif dia == 4:
    print("Hoje é Quinta-feira!")
elif dia == 5:
    print("Hoje é Sexta-feira!")
elif dia == 6:
    print("Hoje é Sábado!")
else:
    print("Você é burro cara?")
